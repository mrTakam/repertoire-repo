void Recherche();

/**
 * @brief Supprime un enregistrement du répertoire.
 *
 * Cette fonction permet à l'utilisateur de supprimer un enregistrement du répertoire
 * en fonction de critères spécifiques.
 *
 * @param void
 * @return void
 */
void Supprimer();


